#include "libft.h"

t_list	*ft_lstlast(t_list *lst)
{
	register t_list	*temp;

	if (!lst)
		return (NULL);
	temp = lst;
	while (temp->next)
	{
		temp = temp->next;
	}
	return (temp);
}
